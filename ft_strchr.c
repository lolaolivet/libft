#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*a;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
		{
			a = (char*)s;
			return (&a[i]);
		}
		i++;
	}
	if (c == '\0')
	{
		a = (char*)s;
		return (&a[i]);
	}
	return (NULL);
}
